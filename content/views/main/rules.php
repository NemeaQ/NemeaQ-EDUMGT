                - Хаотично вырытые ямы или туннели разной глубины и в разных местах (в т.ч. в упор к чужому привату),
                разлив жидкостей, случайным образом расставленные блоки на большой территории и т.п.<br>
                - Создание регионов вблизи приватов других игроков, блокировка выходов из чужого привата любыми
                способами;<br>
                - Создание регионов, которые располагаются вблизи или вплотную, либо сверху или снизу от региона игрока,
                с целью создать ему неудобства.<br>
                В случае жалобы на сайт такой регион будет удалён, а его владелец наказан по данному пункту.<br>
                Постройки в воздухе;
                Допускается постройка тематических сооружений (воздушные шары, космические корабли, самолёты, магические
                летающие острова, драконы и т.п.), но запрещены любые жидкости, льющиеся с них вниз.
