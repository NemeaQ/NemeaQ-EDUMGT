                угоду стабилизации работы сервера и комфорта для всех его игроков.</p>
            <p>4.3 Запрещено нанесение ущерба, порча игрового процесса другим игрокам.
                Заманивание и выталкивание игрока на территорию с включенным PvP с целью убийства или нанесения ущерба
                иным образом;
                Убийство игрока любым способом (лава, ловушка и т.д.) на территории с выключенным PvP-режимом;
