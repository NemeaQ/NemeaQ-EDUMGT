                - Если запривачен основной регион развития игрока, где находится его база, механизмы или ресурсы, и даже
                если в этом регионе запривачена процедурно-генерируемая структура, только при отсутствии 30 и более дней
                регион будет удалён.<br>
                - Если запривачена процедурно-генерируемая структура (сокровищницы, данжи и т.д), при отсутствии 15 и
                более дней регион будет удалён.<br>
                - Если владельцы привата не заходили на сервер достаточно для регенерации привата, но при этом на их
                регионе расположен активный и популярный варп, под это правило он не попадает.
