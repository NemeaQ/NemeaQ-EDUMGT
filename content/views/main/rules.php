                Раздача, продажа, хранение, использование и передача любых ресурсов, добытых нечестным путём;
                Злоупотребление или обход ("абьюз") игровых механик в свою пользу (для получения преимущества и проч.).
                Нашли недоработку? Незамедлительно обратитесь в личные сообщения группы VK.
